#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*src;

	if (!s)
		return (NULL);
	if (start >= ft_strlen(s))
		return (ft_strdup(""));
	if (len > ft_strlen(s + start))
		len = ft_strlen(s + start);
	src = ft_calloc(len + 1, sizeof(char));
	if (!src)
		return (NULL);
	ft_memcpy(src, s + start, len);
	return (src);
}
